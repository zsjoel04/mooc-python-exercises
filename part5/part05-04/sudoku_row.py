

def row_correct(sudoku: list, row_no: int):
    my_list = sudoku[row_no]
    return match_check(my_list)




def match_check(list):
    for square in range(len(list)):
        current = list[square]
        new_list = list[square + 1:]
        for i in new_list:
            if i != 0:
                if i == current:
                    return False
    return True



